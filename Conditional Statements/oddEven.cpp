// Programs to find odd Even of a number?

#include<iostream>
using namespace std;

int main()
{   
     int n;
	cout<<"Enter a number to find Even or Odd: "<<endl;
	cin>>n;
	if(n%2==0)
	{
		cout<<"the given number is a even number: "<<n<<endl;
	}
	else
	{
		cout<<"the given number is a odd number: "<<n<<endl;
	}
	
	
	
	return 0;
}